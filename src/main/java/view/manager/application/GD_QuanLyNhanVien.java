    private void JButtonAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JButtonAddMouseClicked
        // TODO add your handling code here:
        int tuoi = Integer.parseInt(jTextFieldTuoi.getText());

        Boolean gioiTinh = null;
        if (jRadioButtonNam.isSelected()) {
            gioiTinh = true;
        } else if (jRadioButtonNu.isSelected()) {
            gioiTinh = false;
        }

        utils.Enum.LoaiVaiTro vaiTro = null;
        if (jRadioButtonNhanVienQuanLy.isSelected()) {
            vaiTro = utils.Enum.LoaiVaiTro.NHAN_VIEN_QL;
        } else if (jRadioButtonNhanVien.isSelected()) {
            vaiTro = utils.Enum.LoaiVaiTro.NHAN_VIEN_PHUC_VU;
        }

        boolean trangThai = true;
        if (jComboBox1.getSelectedItem().toString().equals("Đang làm")) {
            trangThai = true;
        } else if (jComboBox1.getSelectedItem().toString().equals("Đang nghĩ làm")) {
            trangThai = false;
        }

        NhanVien nv = new NhanVien();
        nv.setHoTen(jTextFieldHoTen.getText());
        nv.setTuoi(tuoi);
        nv.setSoDienThoai(jTextFieldSoDienThoai.getText());
        nv.setNgayBatDauLam(LocalDateTime.now());
        nv.setDiaChi(jTextFieldDiaChi.getText());
        nv.setVaiTro(vaiTro);
        nv.setTrangThai(true);
        nv.setGioiTinh(gioiTinh);
        nv.setTrangThai(trangThai);
        nv.setMaNV(jTextFieldMaNhanVien.getText());
        if (daoNV.insert(nv)) {
            Notifications.getInstance().show(Notifications.Type.SUCCESS, Notifications.Location.TOP_RIGHT, 1500, "Thêm nhân viên thành công !");
            populateTable();
            clearData();
            addTaiKhoan(nv);
            Notifications.getInstance().show(Notifications.Type.SUCCESS, Notifications.Location.TOP_RIGHT, 1500, "Thêm tài khoản thành công !");

        } else {
            Notifications.getInstance().show(Notifications.Type.ERROR, Notifications.Location.TOP_RIGHT, 1500, "Chưa được thêm nhân viên!");

        }
    }//GEN-LAST:event_JButtonAddMouseClicked

