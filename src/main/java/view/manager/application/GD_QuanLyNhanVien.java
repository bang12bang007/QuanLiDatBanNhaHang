    private void populateTable() {
        // Xóa tất cả các dòng hiện có trong bảng
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        List<NhanVien> allNhanVien = daoNV.findAll(NhanVien.class);

        String gioiTinh = "";
        String trangThai = "";
        // Thêm dữ liệu từ danh sách vào bảng
        for (int i = 0; i < allNhanVien.size(); i++) {
            NhanVien nv = allNhanVien.get(i);
            // Truy cập các thuộc tính của đối tượng Ban và thêm vào từng cột
            if (nv.isGioiTinh()) {
                gioiTinh = "Nữ";
            } else {
                gioiTinh = "Nam";
            }
            if (nv.isTrangThai()) {
                trangThai = "Đang làm";
            } else {
                trangThai = "Đã nghĩ làm";
            }
            model.addRow(new Object[]{i + 1, nv.getHoTen(), nv.getTuoi(), nv.getSoDienThoai(), nv.getNgayBatDauLam().toLocalDate().toString(), gioiTinh, nv.getVaiTro().toString(), trangThai, nv.getMaNV(), nv.getDiaChi()});
        }

        // Tạo renderer tùy chỉnh để căn giữa văn bản
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment(JLabel.CENTER);

        // Gán renderer này cho tất cả các cột
        for (int i = 0; i < jTable1.getColumnCount(); i++) {
            jTable1.getColumnModel().getColumn(i).setCellRenderer(centerRenderer);
        }
    }

