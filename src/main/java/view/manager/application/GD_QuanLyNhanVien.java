    private void panelRound3MouseExited(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_panelRound3MouseExited
        // TODO add your handling code here:
        /*
        * NVabbddmmyyxxx Trong đó : a là giới tính (nam là 1, nữ là 0) bb là tuổi
        * của nhân viên lúc vào làm dd là ngày vào làm mm là tháng vào làm yy là 2
         * số cuối của năm vào làm xxx là 3 số đuôi của số điện thoại.
        *    
         */
        String gioiTinh = "";
        if (jRadioButtonNam.isSelected()) {
            gioiTinh = "1";
        } else {
            gioiTinh = "0";
        }

        String vaiTro = "";
        if (jRadioButtonNhanVienQuanLy.isSelected()) {
            vaiTro = "1";
        } else {
            vaiTro = "0";
        }

        String tuoi = jTextFieldTuoi.getText();
        String ngay = String.valueOf(LocalDateTime.now().getDayOfMonth());
        String thang = String.valueOf(LocalDateTime.now().getMonthValue());
        String nam = String.valueOf(LocalDateTime.now().getYear() - 2000);
        String sdt = jTextFieldSoDienThoai.getText();

        // Kiểm tra nếu các trường không rỗng
        if (!gioiTinh.isEmpty() || !tuoi.isEmpty() || !sdt.isEmpty()) {
            String maNhanVien = "NV" + gioiTinh + tuoi + vaiTro + ngay + thang + nam + sdt.substring(7);
            jTextFieldMaNhanVien.setText(maNhanVien);
        }

    }//GEN-LAST:event_panelRound3MouseExited

