    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        List<NhanVien> allNhanVien = daoNV.findAll(NhanVien.class);
        // Lấy chỉ số của hàng được chọn
        int rowIndex = jTable1.getSelectedRow();

        // Kiểm tra nếu hàng được chọn hợp lệ
        if (rowIndex >= 0) {
            // Lấy đối tượng NhanVien từ hàng được chọn
            NhanVien nv = allNhanVien.get(rowIndex);

            // Hiển thị dữ liệu của đối tượng NhanVien lên các JTextField tương ứng
            jTextFieldHoTen.setText(nv.getHoTen());
            jTextFieldTuoi.setText(String.valueOf(nv.getTuoi()));
            jTextFieldSoDienThoai.setText(nv.getSoDienThoai());
            jTextFieldDiaChi.setText(nv.getDiaChi());
            jTextFieldMaNhanVien.setText(nv.getMaNV());

            // Chuyển đổi ngày bắt đầu làm việc từ LocalDateTime sang LocalDate và hiển thị lên JTextField
            jTextFieldNgayVaoLam.setText(nv.getNgayBatDauLam().toLocalDate().toString());

            // Chuyển đổi giới tính từ boolean sang chuỗi và hiển thị lên JTextField
            if (nv.isGioiTinh()) {
                jRadioButtonNam.setSelected(true);
                jRadioButtonNu.setSelected(false);
            } else {
                jRadioButtonNam.setSelected(false);
                jRadioButtonNu.setSelected(true);
            }
            // Hiển thị vai trò của nhân viên lên JTextField
            if (nv.getVaiTro().equals(utils.Enum.LoaiVaiTro.NHAN_VIEN_QL)) {
                jRadioButtonNhanVienQuanLy.setSelected(true);
                jRadioButtonNhanVien.setSelected(false);
            } else {
                jRadioButtonNam.setSelected(false);
                jRadioButtonNu.setSelected(true);
            }
            // Chuyển đổi trạng thái từ boolean sang chuỗi và hiển thị lên JTextField
            if (nv.isTrangThai()) {
                jComboBox1.setSelectedItem("Đang làm");
            } else {
                jComboBox1.setSelectedItem("Đã nghĩ làm");
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void JButtonUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JButtonUpdateMouseClicked
        // TODO add your handling code here:
        int tuoi;
        try {
            tuoi = Integer.parseInt(jTextFieldTuoi.getText());
            if (tuoi < 18) {
                Notifications.getInstance().show(Notifications.Type.WARNING, Notifications.Location.TOP_RIGHT, 1500, "Nhân viên phải >= 18 tuổi!");
                return;
            }
        } catch (NumberFormatException e) {
            Notifications.getInstance().show(Notifications.Type.ERROR, Notifications.Location.TOP_RIGHT, 1500, "Tuổi phải là một số nguyên hợp lệ!");
            return;
        }

        // Kiểm tra họ tên không được rỗng
        String hoTen = jTextFieldHoTen.getText();
        if (hoTen.isEmpty()) {
            Notifications.getInstance().show(Notifications.Type.WARNING, Notifications.Location.TOP_RIGHT, 1500, "Họ tên không được rỗng!");
            return;
        }

        // Kiểm tra số điện thoại
        String soDienThoai = jTextFieldSoDienThoai.getText();
        if (!soDienThoai.matches("^(03|05|07|08|09)\\d{8}$")) {
            Notifications.getInstance().show(Notifications.Type.ERROR, Notifications.Location.TOP_RIGHT, 1500, "Số điện thoại không hợp lệ! Phải bắt đầu bằng 03, 05, 07, 08, 09 và có 10 số.");
            return;
        }

        // Kiểm tra ngày bắt đầu làm việc
        LocalDateTime ngayBatDauLam = LocalDateTime.now();  // Hoặc nhận từ một input nào đó nếu cần
        if (ngayBatDauLam.isAfter(LocalDateTime.now())) {
            Notifications.getInstance().show(Notifications.Type.ERROR, Notifications.Location.TOP_RIGHT, 1500, "Ngày bắt đầu làm việc không được vượt quá ngày hiện tại!");
            return;
        }

        // Kiểm tra địa chỉ không được rỗng
        String diaChi = jTextFieldDiaChi.getText();
        if (diaChi.isEmpty()) {
            Notifications.getInstance().show(Notifications.Type.WARNING, Notifications.Location.TOP_RIGHT, 1500, "Địa chỉ không được rỗng!");
            return;
        }

        // Tạo đối tượng NhanVien và cập nhật thông tin
        NhanVien nv = new NhanVien();
        nv.setHoTen(hoTen);
        nv.setTuoi(tuoi);
        nv.setSoDienThoai(soDienThoai);
        nv.setNgayBatDauLam(ngayBatDauLam);
        nv.setDiaChi(diaChi);

        // Cập nhật thông tin nhân viên
        if (daoNV.update(nv)) {
            Notifications.getInstance().show(Notifications.Type.SUCCESS, Notifications.Location.TOP_RIGHT, 1500, "Cập nhật thông tin nhân viên thành công!");
            populateTable();  // Giả sử phương thức này cập nhật lại bảng hiển thị nhân viên
            clearData();      // Giả sử phương thức này xóa dữ liệu trong các trường nhập
        } else {
            Notifications.getInstance().show(Notifications.Type.ERROR, Notifications.Location.TOP_RIGHT, 1500, "Cập nhật thông tin nhân viên thất bại!");
        }
    }//GEN-LAST:event_JButtonUpdateMouseClicked

